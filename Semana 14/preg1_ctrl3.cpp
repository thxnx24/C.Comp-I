/*Defina los siguientes conceptos

- Puntero This: Es un puntero inplicito que apunta al objeto 
actual dentro de una función miembro de una clase. Se utiliza para 
acceder y manipular los miembros del objeto dentro de la función.

- Destructor: Un destructor es una función miembro especial de una clase
la cual se utiliza para liberar recursos cuando un objeto se destruye.

- Memory Leak: Es cuando ya se ocupó todo el espacio de memoria y ya no queda
espacio en la memoria para reservar.

- Función/clase friend;
    Permite que una función o clase tenga acceso directo a miembros 
    privados de otra clase

- Dangling Pointer:
    Es un puntero que apunta a memoria cuyo contenido no está definido.
*/